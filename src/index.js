// Background polygonal shapes — drawn once, behind all hands
function drawBackgroundShapes() {
  // Pick colors once and reuse across all shapes for a coherent palette
  const colorCount  = Math.floor(brush.random(3, 6));
  const shapeColors = [];
  const usedSet     = new Set();
  while (shapeColors.length < colorCount) {
    const c = brush.random(palette);
    if (!usedSet.has(c)) { usedSet.add(c); shapeColors.push(c); }
  }

  // Insert interpolated points every ~step px along each edge of a closed polygon
  const subdivide = (pts, step = 50) => {
    const result = [];
    for (let i = 0; i < pts.length; i++) {
      const [x1, y1] = pts[i];
      const [x2, y2] = pts[(i + 1) % pts.length];
      const n = Math.max(1, Math.ceil(Math.hypot(x2 - x1, y2 - y1) / step));
      for (let t = 0; t < n; t++) {
        result.push([x1 + (x2 - x1) * (t / n), y1 + (y2 - y1) * (t / n)]);
      }
    }
    return result;
  };

  // Shape makers — centered at (cx, cy)
  const makeRect = (cx, cy) => {
    const rw = w * brush.random(...RECT_SCALE_W);
    const rh = h * brush.random(...RECT_SCALE_H);
    const x = cx - rw / 2, y = cy - rh / 2;
    // Slight independent slant on each corner
    const slant = Math.min(rw, rh) * 0.06;
    const corners = [
      [x          + brush.random(-slant, slant), y          + brush.random(-slant, slant)],
      [x + rw     + brush.random(-slant, slant), y          + brush.random(-slant, slant)],
      [x + rw     + brush.random(-slant, slant), y + rh     + brush.random(-slant, slant)],
      [x          + brush.random(-slant, slant), y + rh     + brush.random(-slant, slant)],
    ];
    // Subdivide edges then add small per-point jitter
    const jitter = Math.min(w, h) * 0.0015;
    const pts = subdivide(corners, 160).map(([px, py]) => [
      px + brush.random(-jitter, jitter),
      py + brush.random(-jitter, jitter),
    ]);
    return { curved: false, pts };
  };
  const makeBlob = (cx, cy) => {
    const rx = w * brush.random(...BLOB_SCALE);
    const ry = h * brush.random(...BLOB_SCALE);
    return { curved: true, pts: Array.from({length: 8}, (_, i) => {
      const a = (i / 8) * Math.PI * 2;
      return [cx + Math.cos(a)*rx*brush.random(0.7,1.3), cy + Math.sin(a)*ry*brush.random(0.7,1.3)];
    })};
  };
  const makers = [makeRect, makeRect, makeBlob, makeBlob, makeBlob];
  const randomMaker = () => makers[Math.floor(brush.random(makers.length))];

  // --- Build position list ---
  const centers = []; // [cx, cy] for all shapes

  // 1. One tight cluster of 2–3 shapes
  const clusterSize   = Math.floor(brush.random(2, 3));
  const clusterCX     = brush.random(w * 0.15, w * 0.85);
  const clusterCY     = brush.random(h * 0.15, h * 0.85);
  const clusterSpread = Math.min(w, h) * 0.26;
  for (let i = 0; i < clusterSize; i++) {
    centers.push([
      clusterCX + brush.random(-clusterSpread, clusterSpread),
      clusterCY + brush.random(-clusterSpread, clusterSpread),
    ]);
  }

  // 2. Remaining shapes Poisson-distributed away from each other and the cluster
  const freeCount = Math.floor(brush.random(2, 6));
  const MIN_DIST  = Math.min(w, h) * 0.35;
  for (let i = 0; i < freeCount; i++) {
    let best = null, bestScore = -Infinity;
    for (let attempt = 0; attempt < 60; attempt++) {
      const cx = brush.random(-w * 0.05, w * 1.05);
      const cy = brush.random(-h * 0.05, h * 1.05);
      let score = Infinity;
      for (const [ax, ay] of centers) score = Math.min(score, Math.hypot(cx-ax, cy-ay) - MIN_DIST);
      if (score >= 0) { best = [cx, cy]; break; }
      if (score > bestScore) { bestScore = score; best = [cx, cy]; }
    }
    centers.push(best);
  }

  // --- Pre-build all shapes, then draw rects first, blobs on top ---
  const allShapes = centers.map(([cx, cy]) => ({
    shape: randomMaker()(cx, cy),
    color: brush.random(shapeColors),
  }));
  // Each symbol appears at most once (~50% chance each)
  for (const sym of archSyms) {
    if (brush.random(1) > 0.5) {
      allShapes.push({
        shape: { curved: false, pts: mapSym(sym, brush.random(w * 0.05, w * 0.95), brush.random(h * 0.05, h * 0.95)) },
        color: brush.random(shapeColors),
      });
    }
  }
  const sorted = [
    ...allShapes.filter(s => !s.shape.curved),
    ...allShapes.filter(s =>  s.shape.curved),
  ];

  for (const { shape, color } of sorted) {
    function drawShape () {
      const curvature = shape.curved ? brush.random(0.6, 1) : 0;
      brush.beginPath(curvature);
      brush.moveTo(shape.pts[0][0], shape.pts[0][1]);
      for (let j = 1; j < shape.pts.length; j++) brush.lineTo(shape.pts[j][0], shape.pts[j][1]);
      brush.closePath();
      brush.endPath();
    }

    brush.noStroke();

    // Fill or erase with color
    let filling = brush.random(1) > 0.65 || shape.curved;
    if (filling) {
      brush.erase(bgColor, 100);
      drawShape();
      brush.noErase();
      brush.draw();
      brush.fillStyle(color, 150);
      brush.fillBleed(0.05);
      brush.fillTexture(0.1, 0.05);
    } else {
      brush.erase(color, 100);
      drawShape();
      brush.draw();
      brush.noErase();
    }
    drawShape();
    brush.noFill();
    brush.noHatch();
    brush.draw();
  }

}

// Map a symbol to canvas space: center at (px, py), scaled by targetSize/span, slight rotation
function mapSym({ pts, cx, cy, span }, px, py) {
  const s = (Math.min(w, h) * brush.random(...SYMBOL_SCALE)) / span;
  const angle = brush.random(-Math.PI / 20, Math.PI / 20);
  const cos = Math.cos(angle), sin = Math.sin(angle);
  return pts.map(([x, y]) => {
    const dx = (x - cx) * s, dy = (y - cy) * s;
    return [px + cos * dx - sin * dy, py + sin * dx + cos * dy];
  });
}

// Composed mode: single large hand over sky / sun / earth
function drawComposedScene() {
  brush.wiggle(0)
  // Pick 3 distinct palette colors
  const usedColors = new Set();
  const pickColor = () => {
    let c;
    do { c = brush.random(palette); } while (usedColors.has(c));
    usedColors.add(c);
    return c;
  };
  const skyColor   = pickColor();
  const sunColor   = pickColor();
  const earthColor = pickColor();

  const drawPoly = (pts, curvature = 0) => {
    brush.beginPath(curvature);
    brush.moveTo(pts[0][0], pts[0][1]);
    for (let i = 1; i < pts.length; i++) brush.lineTo(pts[i][0], pts[i][1]);
    brush.closePath();
    brush.endPath();
  };

  const fillShape = (pts, color, curvature = 0, isBlob) => {
    brush.noStroke();

    if (isBlob) {
      brush.erase(bgColor, 100);
      drawPoly(pts, curvature);
      brush.noErase();
      brush.draw();
      brush.fillStyle(color, 150);
      brush.fillBleed(0.05);
      brush.fillTexture(0.1, 0.15);
    } else {
      brush.erase(color, 100);
      drawPoly(pts, curvature);
      brush.draw();
      brush.noErase();
    }
    drawPoly(pts, curvature);
    brush.noFill();
    brush.draw();
    brush.noErase();
    brush.noHatch();
  };

  // 1. Sky — rect where each edge independently may be inset or flush to canvas edge
  const skyH  = brush.random(0.52, 0.65);
  const mX    = w * brush.random(0.04, 0.08);
  const mY    = h * brush.random(0.04, 0.08);
  const bleed = Math.min(w, h) * 0.02; // overshoot past canvas edge to avoid gaps
  const skyX0 = brush.random(1) > 0.5 ? mX : -bleed;
  const skyX1 = brush.random(1) > 0.5 ? w - mX : w + bleed;
  const skyY0 = brush.random(1) > 0.5 ? mY : -bleed;
  const skyY1 = h * skyH;
  const jAmt  = Math.min(w, h) * 0.005;
  const nSeg  = 6;
  const organicEdge = (x0, y0, x1, y1, jx, jy) =>
    Array.from({ length: nSeg - 1 }, (_, i) => {
      const t = (i + 1) / nSeg;
      return [x0 + (x1 - x0) * t + brush.random(-jx, jx),
              y0 + (y1 - y0) * t + brush.random(-jy, jy)];
    });
  const skyPts = [
    [skyX0, skyY0],
    ...organicEdge(skyX0, skyY0, skyX1, skyY0, jAmt * 0.3, jAmt),      // top
    [skyX1, skyY0],
    ...organicEdge(skyX1, skyY0, skyX1, skyY1, jAmt, jAmt * 0.3),      // right
    [skyX1, skyY1],
    ...organicEdge(skyX1, skyY1, skyX0, skyY1, jAmt * 0.3, jAmt),      // bottom
    [skyX0, skyY1],
    ...organicEdge(skyX0, skyY1, skyX0, skyY0, jAmt, jAmt * 0.3),      // left
  ];
  fillShape(skyPts, skyColor);

  // 2. Sun — blob in upper-right area
  const sunCX = brush.random(w * 0.62, w * 0.85);
  const sunCY = brush.random(h * 0.04, h * 0.20);
  const sunRX = w * brush.random(...SUN_SCALE_X);
  const sunRY = h * brush.random(...SUN_SCALE_Y);
  const sunPts = Array.from({ length: 8 }, (_, i) => {
    const a = (i / 8) * Math.PI * 2;
    return [sunCX + Math.cos(a) * sunRX * brush.random(0.75, 1.25),
            sunCY + Math.sin(a) * sunRY * brush.random(0.75, 1.25)];
  });
  fillShape(sunPts, sunColor, brush.random(0.6, 1), true);

  // 3. Earth — scaled so bounding-box width matches the sky width, centered on sky
  const earthRefW   = 1890 - 147; // reference bounding-box width of all earthShapes
  const earthScale  = (skyX1 - skyX0) / earthRefW;
  const earthPosX   = (skyX0 + skyX1) / 2;
  const earthPosY   = h * brush.random(0.65, 0.70);
  const earthJitter = 2 * earthScale;
  const te = (pts) => transformPoints(pts, earthPosX, earthPosY, earthScale, earthJitter, 1, 0, earthShapeCX, earthShapeCY, 2.4);

  // Main terrain — always drawn
  fillShape(te(earthShape1), earthColor, 1);

  // Secondary shapes — each shown independently (~60% chance) with randomised geometry
  // varyShape: per-point Y jitter + global Y shift + horizontal stretch around shape centre
  const varyShape = (pts, cx, xShift = 0) => {
    const xScale = brush.random(0.65, 1.35);
    const yJit   = brush.random(3, 10);
    return pts.map(([px, py]) => [
      cx + (px - cx) * xScale + xShift + brush.random(-6, 6),
      py + brush.random(-yJit, yJit),
    ]);
  };
  const secondary = [
    { shape: earthShape2, cx: 305,  xShift: 0 },
    { shape: earthShape3, cx: 968,  xShift: brush.random(-120, 120) },
    { shape: earthShape4, cx: 1031, xShift: brush.random(-120, 120) },
  ];
  for (const { shape, cx, xShift } of secondary) {
    if (brush.random(1) > 0.4) fillShape(te(varyShape(shape, cx, xShift)), earthColor, 1);
  }

  // Each decorative symbol appears at most once (~50% chance each)
  for (const sym of archSyms) {
    if (brush.random(1) > 0.5) {
      const px = brush.random(skyX0, skyX1);
      const py = brush.random(skyY0, skyY1);
      fillShape(mapSym(sym, px, py), sunColor);
    }
  }

  brush.draw();
}

